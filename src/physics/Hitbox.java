package physics;

public class Hitbox {
	
	public int x;
	public int y;
	public int width;
	public int height;
	
	public Hitbox(int x, int y, int width, int height){
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
